void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	//increase
	if (GPIO_Pin == KEY1_Pin) {
		if (curr_led_ptr < LED_COUNT) {
			led_state[curr_led_ptr] = 1;
			curr_led_ptr++;
		}
	}
	//decrease
	else if (GPIO_Pin == KEY2_Pin) {
		if (curr_led_ptr > 0) {
			curr_led_ptr--;
			led_state[curr_led_ptr] = 0;
		}
	}
}
