void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	static SERVO_t servo1 = {0, FORWARD_DIRECTION};
	static SERVO_t servo2 = {0, FORWARD_DIRECTION};
	if (htim->Instance == TIM1) {
		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, deg_to_us(servo1.angle));
		if (servo1.direction == FORWARD_DIRECTION) {
			servo1.angle++;
			if (servo1.angle == 180) servo1.direction = BACKWARD_DIRECTION;
		}
		else if (servo1.direction == BACKWARD_DIRECTION) {
			servo1.angle--;
			if (servo1.angle == 0) servo1.direction = FORWARD_DIRECTION;
		}
	}

	if (htim->Instance == TIM3) {
			__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, deg_to_us(servo2.angle));
			if (servo2.direction == FORWARD_DIRECTION) {
				servo2.angle++;
				if (servo2.angle == 180) servo2.direction = BACKWARD_DIRECTION;
			}
			else if (servo2.direction == BACKWARD_DIRECTION) {
				servo2.angle--;
				if (servo2.angle == 0) servo2.direction = FORWARD_DIRECTION;
			}
		}
}

